----------------------------------------------------------
-- RLS POLICIES
----------------------------------------------------------

CREATE POLICY "Users can read own XP events"
ON public.xp_events
FOR SELECT
TO authenticated
USING (auth.uid() = user_id);

CREATE POLICY "Authenticated users can read achievements"
ON public.achievements
FOR SELECT
TO authenticated
USING (true);

CREATE POLICY "Users can read own achievements"
ON public.user_achievements
FOR ALL
TO authenticated
USING (auth.uid() = user_id)
WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Authenticated users can read missions"
ON public.daily_missions
FOR SELECT
TO authenticated
USING (true);

CREATE POLICY "Users can manage own daily missions"
ON public.user_daily_missions
FOR ALL
TO authenticated
USING (auth.uid() = user_id)
WITH CHECK (auth.uid() = user_id);

